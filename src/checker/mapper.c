int check_map(t_data *data)
{
    int x;
    int y;

    y = 0;
    while (y < data->map_h - 1)
    {
        x = 0;
        while (x < data->map_w - 1)
        {
            if (data->map[y][x] == ' ')
                x++;
        
        }
        y++;
    }
    return (1);
}